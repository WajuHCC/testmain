#liczby całkowite - intereger
print(type(10))
print(int())
print(int("10"))
print(int("10",base=3))
print(int(2.7))

#liczby zmiennoprzecinkowe - float
print(float())
print(float(1/3))
print(0.1 + 0.1 + 0.1)
print(0.1 + 0.1 + 0.1 == 0.3)

#liczby zespolone: complex
print(complex())
print(10 + 10j)

#wartośći boolowskie: bool True False
print(bool())
print(10 + True)
print(True and False)
print(not(True))
print(True or False)
print(not(True))
